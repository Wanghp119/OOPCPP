// 定义星期枚举变量
enum eWeekday
{
	SUN, MON, TUE, WED, THR, FRI, SAT
};

eWeekday GetWeekday(int nYear, int nMonth, int nDay);

int GetMonthDays(int nYear, int nMonth);

// 判断闰年
inline bool IsLeapYear(int nYear)
{
	return ((nYear%4==0 && nYear%100!=0) || nYear%400==0);
}

